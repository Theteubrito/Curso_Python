# Operadores relacionais (comparação)
'''
OP -------Significado------- Exemplo (true)
> ------------Maior--------- 2 > 1
>= -----Maior ou igual------ 2 >= 2
< -----------Menor---------- 1 < 2
<= -----Menor ou igual------ 2 <= 2
== ----------Igual---------- 'a' == 'a'
!= --------Diferente-------- 'a' != 'b'


Operadores  relacionais comparam dois valores retornado uma bool (true ou false)

Ex:
'''
